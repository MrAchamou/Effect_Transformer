    this.temps += deltaTime;
    
    // Mise à jour logique de l'effet
    this.updateEffectLogic(deltaTime);
    
    // Optimisation adaptative
    this.adaptiveOptimization(deltaTime);
    
    // Mise à jour statistiques
    this.updateStatistics();
